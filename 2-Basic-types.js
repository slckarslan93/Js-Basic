//* Number Veri Tipi -------------------------

const num1 = 10;
const num2 = 22;

console.log(num1 + num2);
console.log(typeof (num1 + num2));

//* String Veri Tipi -------------------------

const a = "Selçuk";
const b = "Arslan";

console.log(a + " " + b);
console.log(typeof (a + " " + b));

//* Boolean Veri Tipi ------------------------

const bool1 = true;
const bool2 = false;

console.log(bool1 == bool2);
console.log(typeof bool1);

//* Undefined Veri Tipi ----------------------
const u1 = undefined;
let u2 = undefined; //! henüz kullanmadığımız değişkenlerimize atadığımız tip daha sonra değer atama istediğimiz bir değişken olabilir.

console.log(typeof u2);

//* Null Veri Tipi  ---------------------------

const n1 = null;
let n2 = null;

console.log(typeof n2); //! geri object döner null veri tipi.
